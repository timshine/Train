
public class Point {
	int x;
   	int y;

   	//Default Constructor
   	public Point()   {   x = 0;   y = 0;   }

   	//Overloaded Constructor
   	public Point(int x, int y)
   	{
      	this.x = x;
      	this.y = y;
   	}

   	//Get Methods for Point class
 	public int getX()   {   return x;   }
   
   	public int getY()   {   return y;   }

}
